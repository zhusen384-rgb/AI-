/**
 * 从题库中随机抽取指定数量的题目
 * @param count 需要抽取的题目数量
 * @param excludeIds 需要排除的题目ID（已抽过的题目）
 * @returns 随机抽取的题目数组
 */
export function getRandomTechnicalQuestions(
  count: number = 3,
  excludeIds: number[] = []
): TechnicalQuestion[] {
  // 过滤掉已抽过的题目
  const availableQuestions = TECHNICAL_QUESTIONS.filter(
    q => !excludeIds.includes(q.id)
  );

  // 如果可用题目不足，返回所有可用题目
  if (availableQuestions.length <= count) {
    return [...availableQuestions];
  }

  // Fisher-Yates 洗牌算法随机抽取
  const shuffled = [...availableQuestions];
  for (let i = shuffled.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [shuffled[i], shuffled[j]] = [shuffled[j], shuffled[i]];
  }

  return shuffled.slice(0, count);
}

/**
 * 根据ID获取题目
 */
export function getQuestionById(id: number): TechnicalQuestion | undefined {
  return TECHNICAL_QUESTIONS.find(q => q.id === id);
}
